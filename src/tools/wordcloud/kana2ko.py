_SMALL = {"ァ": "ㅏ", "ィ": "ㅣ", "ゥ": "ㅜ", "ェ": "ㅔ", "ォ": "ㅗ"}


def _to_kata(s: str) -> str:
    """히라가나를 가타카나로 정규화."""
    return "".join(chr(ord(ch) + 0x60) if 0x3041 <= ord(ch) <= 0x3096 else ch
                   for ch in s)


def kana2ko(text: str) -> str:
    """가나 문자열 → 한글 음차. 가나가 아닌 문자는 건너뛴다."""
    s = _to_kata(text)
    out: list[list[str]] = []          # [초성, 중성, 종성]
    i, n = 0, len(s)
    while i < n:
        ch = s[i]
        if i + 1 < n and s[i:i + 2] in _YOUON:        # 요음/복합 2글자 우선
            cho, jung = _YOUON[s[i:i + 2]]
            out.append([cho, jung, ""])
            i += 2
        elif ch == "ッ":                               # 촉음 → 종성 ㅅ
            if out and out[-1][2] == "":
                out[-1][2] = "ㅅ"
            i += 1
        elif ch == "ン":                               # 발음 → 종성 ㄴ
            if out and out[-1][2] == "":
                out[-1][2] = "ㄴ"
            else:
                out.append(["ㅇ", "ㅡ", "ㅇ"])          # 응
            i += 1
        elif ch == "ー":                               # 장음 무시
            i += 1
        elif ch in _BASE:
            cho, jung = _BASE[ch]
            out.append([cho, jung, ""])
            i += 1
        elif ch in _SMALL:
            out.append(["ㅇ", _SMALL[ch], ""])
            i += 1
        else:                                          # 가나 아님 → 건너뜀
            i += 1
    return "".join(compose(c, v, t) for c, v, t in out)


