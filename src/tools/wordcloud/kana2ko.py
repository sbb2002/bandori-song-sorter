"""가나(가타카나/히라가나) → 한글 음차 변환.

목적: 가사 보컬리제·의성어(ドント·ジャーン·カイ·レッツ…)를 의미번역(align/MT) 대신
일어 한글음차로 표기. 잘 정착된 외래어는 build_keywords.py가 align/MT를 우선하므로,
이 변환기는 align 실패 + 가나전용 토큰(진짜 보컬리제)에만 쓰인다.

음차 관례: 카/타/츠 등 거센소리, 장음(ー) 무시, 촉음(ッ)·발음(ン)은 종성으로.
"""
from __future__ import annotations

CHO = "ㄱㄲㄴㄷㄸㄹㅁㅂㅃㅅㅆㅇㅈㅉㅊㅋㅌㅍㅎ"
JUNG = "ㅏㅐㅑㅒㅓㅔㅕㅖㅗㅘㅙㅚㅛㅜㅝㅞㅟㅠㅡㅢㅣ"
JONG = ["", "ㄱ", "ㄲ", "ㄳ", "ㄴ", "ㄵ", "ㄶ", "ㄷ", "ㄹ", "ㄺ", "ㄻ", "ㄼ",
        "ㄽ", "ㄾ", "ㄿ", "ㅀ", "ㅁ", "ㅂ", "ㅄ", "ㅅ", "ㅆ", "ㅇ", "ㅈ",
        "ㅊ", "ㅋ", "ㅌ", "ㅍ", "ㅎ"]
C = {c: i for i, c in enumerate(CHO)}
V = {v: i for i, v in enumerate(JUNG)}
T = {t: i for i, t in enumerate(JONG)}


def compose(cho: str, jung: str, jong: str = "") -> str:
    return chr(0xAC00 + (C[cho] * 21 + V[jung]) * 28 + T[jong])


